func ReverseLevel(g *Game) {
    fmt.Printf("pframe %d/%d\n", 0, len(g.playerAi))
    g.SetReversing()
}

func playTheEnd(g *Game) {
    g.animStart = g.time
    g.state = END
}

func afterReversed(g *Game) {
    g.shaderName = "none"
    g.ResetAll()
    g.playerAiIdx = 0
    g.TransitionState()
}

func levelStart(g *Game) {
    g.ResetAll()
    g.playerAiIdx = 0
    g.SetInGame()
    for _, o := range g.objects {
        o.movable = false
    }
}

